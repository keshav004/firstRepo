export class Person {  
    Name: string;
    Email: string;
    Dob: Date;
    Avatar: string;
    Country: string;
    Gender: string;

    constructor( Name: string,Email: string,Dob: Date,Avatar: string,Country: string,Gender : string){
        this.Name = Name;
        this.Email = Email;
        this.Dob = Dob;
        this.Avatar = Avatar;
        this.Country = Country;
        this.Gender = Gender;
    }
}
 